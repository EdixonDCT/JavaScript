let a = parseInt(prompt("Ingrese el valor de A"));
let b = parseInt(prompt("Ingrese el valor de B"));
let c = parseInt(prompt("Ingrese el valor de C"));
let x = parseInt(prompt("Ingrese el valor de X el cual es constante"));
let result = a * (x*x) + b + x + c;
alert(`El resultado con los valores ingresados y constante es ${result}`);
